    def get_response(self, user_message):
        try:
            chat = Chat(
                messages=[
                    Messages(role=MessagesRole.SYSTEM, content=self.system_prompt),
                    Messages(role=MessagesRole.USER, content=user_message)
                ],
                temperature=0.7,
                max_tokens=1000
            )
            
            response = self.client.chat(chat)
            
            if response and response.choices:
                return response.choices[0].message.content
            else:
                logger.error("Получен пустой ответ от GigaChat")
                return "Извините, я получил пустой ответ. Пожалуйста, попробуйте переформулировать вопрос."
                
        except Exception as e:
            logger.error(f"Ошибка GigaChat: {e}")
            return "Извините, в данный момент я не могу обработать ваш запрос. Пожалуйста, попробуйте позже."
    
    def __del__(self):
        if hasattr(self, 'client'):
            try:
                self.client.close()
            except:
                pass